package interfacce;


/* L'interfaccia è un meccanismo attraverso il quale si possono definire nuovi tipi.
 * Per definire un nuovo tipo si deve specificare un insieme di metodi. 
 * Meglio dire che l'interfaccia specifica un tipo in termini dei servizi, ovvero dei metodi,
 * che questi può offrire. 
 * L'interfaccia non specifica i dettagli implementativi ma solamente in che modo i servizi possono
 * essere invocati.
 */
public interface Strumento {
	public void produciSuono();
}
